  /// Perforated Metal texture
  perforatedMetal,

  /// Roadways texture
  roadways,

  /// Rubber texture
  rubber,

  /// Rusted Metal texture
  rustedMetal,

  /// Sand texture
  sand,

  /// Silk texture
  silk,

  /// Solar System texture
  solarSystem,

  /// Spider Web texture
  spiderWeb,

  /// Spirals texture
  spirals,

  /// Star texture
  star,

  /// Steel texture
  steel,

  /// Suede texture
  suede,

  /// Sunset texture
  sunset,

  /// Train Tracks texture
  trainTracks,

  /// Water texture
  water,

  /// Wood Grain texture
  woodGrain,

  /// Zebra texture
  zebra,
}
