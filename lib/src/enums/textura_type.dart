/// Enum representing the different types of textures available.
/// Each value corresponds to a unique texture that can be applied as a
///  background.
///
/// Sample Usage:
/// ```dart
/// Textura(
///   textureType: TextureType.asphalt,
///   child: YourWidget(),
/// )
/// ```
///
/// In this example, `YourWidget()` will be rendered with the asphalt texture
/// applied as the background.
