  /// Asphalt texture
  asphalt,

  /// Brick Wall texture
  brickWall,

  /// Bubble texture
  bubble,

  /// Camouflage texture
  camo,

  /// Canvas texture
  canvas,

  /// Celtic Knots texture
  celticKnots,

  /// Chequered texture
  chequered,

  /// Chessboard texture
  chessboard,

  /// Circuitry texture
  circuitry,

  /// City Maps texture
  cityMaps,

  /// Clouds texture
  clouds,

  /// Concrete texture
  concrete,

  /// Constellations texture
  constellations,

  /// Denim texture
  denim,

  /// DNA texture
  dna,

  /// Fabric Texture
  fabricTexture,

  /// Foam texture
  foam,

  /// Furry texture
  furry,

  /// Galaxy texture
  galaxy,

  /// Game Pixels texture
  gamePixels,

  /// Glitch texture
  glitch,

  /// Graffiti texture
  graffiti,

  /// Granite texture
  granite,

  /// Graph Paper texture
  graphPaper,

  /// Grass texture
  grass,

  /// Greek Keys texture
  greekKeys,

  /// Grid texture
  grid,

  /// Holographic texture
  holographic,

  /// Honeycomb texture
  honeycomb,

  /// Leather texture
  leather,

  /// Leaves texture
  leaves,

  /// Leopard texture
  leopard,

  /// Linen texture
  linen,

  /// Marble texture
  marble,

  /// Metal Texture
  metalTexture,

  /// Mosaic texture
  mosaic,

  /// Mud texture
  mud,

  /// Music Sheet texture
  musicSheet,

  /// Newsprint texture
  newsprint,

  /// Paper texture
